// Populate areas dynamically in the dropdown
window.onload = () => {
    const areaSelect = document.getElementById("areaSelect");

    Object.keys(officersData).forEach(officer => {
        officersData[officer].areas.forEach(area => {
            const option = document.createElement("option");
            option.value = area;
            option.textContent = area;
            areaSelect.appendChild(option);
        });
    });
};

// Fetch officer details based on selected area
function fetchOfficerData() {
    const selectedArea = document.getElementById("areaSelect").value;
    displayOfficerDetails(selectedArea);
}

// Search area dynamically
function searchArea() {
    const query = document.getElementById("searchArea").value.toLowerCase();
    displayOfficerDetails(query);
}

// Display officer details based on input
function displayOfficerDetails(area) {
    const officerDetails = document.getElementById("officerDetails");
    officerDetails.innerHTML = ""; // Clear previous data

    if (area) {
        let found = false;

        Object.entries(officersData).forEach(([officer, details]) => {
            const matchedAreas = details.areas.filter(a =>
                a.toLowerCase().includes(area.toLowerCase())
            );

            if (matchedAreas.length > 0) {
                found = true;
                const areasHTML = details.areas
                    .map(a =>
                        `<div class="area ${a.toLowerCase() === area.toLowerCase() ? "highlight" : ""}">${a}</div>`
                    )
                    .join("");

                const officerInfo = `
                    <p><strong style="color: rgb(3, 33, 125);">OFFICER NAME :</strong> <span style="color: rgb(3, 33, 125);">${officer}</span></p>
                    <p><strong style="color: rgb(3, 33, 125);">CONTACT:</strong> <span style="color: rgb(3, 33, 125);">${details.phone}</span></p>
                    <div class="areas-container">${areasHTML}</div>
                `;
                officerDetails.innerHTML = officerInfo;
            }
        });

        if (!found) {
            officerDetails.innerHTML = `<p style="color: red;">No results found for "${area}".</p>`;
        }
    }
}
